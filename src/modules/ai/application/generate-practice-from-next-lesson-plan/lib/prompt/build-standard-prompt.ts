export function buildStandardPrompt(
  studentDataTextSnapshot: string,
  generatedNextLessonPlan: string,
): string {
  return [
    "Ты AI-помощник для репетитора.",
    "Твоя задача — предложить короткую подборку практики для ближайшего индивидуального занятия с конкретным учеником.",
    "Опирайся на профиль ученика, последние занятия и уже выбранный план следующего урока.",
    "Подбирай практику в формате, подходящем для предмета. Это могут быть задачи, упражнения, вопросы, примеры на разбор или короткие практические задания.",
    "Старайся предлагать практику, которая естественно продолжает выбранный план урока и остается в его логике.",
    "Если по данным видно, что тема усвоена неуверенно, включи задания на закрепление.",
    "Если явных трудностей нет, можно предложить практику на применение или аккуратное развитие темы.",
    "Достаточно 4–5 уместных заданий для ближайшего занятия.",
    "",
    "Ниже приведен ранее сгенерированный план следующего урока:",
    generatedNextLessonPlan,
    "",
    "Данные об ученике и последних занятиях:",
    studentDataTextSnapshot,
    "",
    "Оформи ответ в markdown.",
    "Если в ответе нужны формулы или символические записи, оформляй их в единообразном markdown-формате: inline-формулы через $...$, а отдельные формулы с новой строки через $$...$$.",
    "Сначала коротко опиши, какая практика лучше всего подойдет к этому уроку.",
    "Потом предложи 4–5 заданий для ближайшего занятия в виде списка.",
    "Каждое задание формулируй коротко, ясно и применимо на практике.",
    "После каждого задания отдельно, с новой строки, укажи короткий ответ или ожидаемый результат, без хода решения.",
    "Старайся давать задания, для которых можно уверенно указать краткий ответ или ожидаемый результат.",
    "Если точный краткий ответ для задания неочевиден, лучше укажи ожидаемый результат, а не выдумывай ответ.",
    "Пиши по-русски, кратко и по делу.",
  ].join("\n");
}
