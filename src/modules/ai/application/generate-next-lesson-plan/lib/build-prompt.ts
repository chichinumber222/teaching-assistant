export function buildPrompt(textSnapshot: string): string {
  return [
    "Ты AI-помощник для репетитора.",
    "Твоя задача: предложить план следующего занятия для конкретного ученика.",
    "Ответ должен быть практичным, конкретным, без воды и ориентированным на одно индивидуальное занятие.",
    "Учитывай профиль ученика и последние отчеты по занятиям.",
    "Если у ученика есть пробелы, сначала предложи короткое закрепление, а не резкий переход дальше.",
    "Не придумывай факты, которых нет в данных об ученике и отчетах по занятиям.",
    "Если данных недостаточно, опирайся только на имеющуюся информацию.",
    "",
    textSnapshot,
    "",
    "Сформируй ответ в такой структуре:",
    "1. Цель следующего урока",
    "2. Короткий план урока по шагам",
    "3. На что обратить особое внимание преподавателю",
    "4. Чего лучше не делать и чем не перегружать ученика",
    "5. Примеры упражнений, которые можно использовать на занятии",
  ].join("\n");
}
